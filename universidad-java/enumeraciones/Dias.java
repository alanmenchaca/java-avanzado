package enumeraciones;

/* Una enumeración es una clase de Java, solo que en ves de la palabra class se utiliza la 
palabra resrvada enum. */
public enum Dias {
    // Constantes de los dias de la semana.
    /* Los tipos enumerados son un tipo especial de clase, el cual nos permite
    asociar una serie de valores de tipo final (constantes). */
    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO
    // Las constantes en Java se escriben en mayúsculas (es considerada una buena práctica).
}
